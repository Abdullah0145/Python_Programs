# If-Elif-Else Statement in Python
# An if-elif-else statement is a conditional statement that allows you to execute different blocks of code based on multiple conditions. The syntax for an if-elif-else statement in Python is as follows:
# if condition1:
#     # block of code to be executed if condition1 is true
# elif condition2:
#     # block of code to be executed if condition2 is true
# elif condition3:
#     # block of code to be executed if condition3 is true
# else:
#     # block of code to be executed if all conditions are false
# For example:
color = "pink"

if color == "red":
    print("The color is red.") # Output: The color is red.
elif color == "blue":
    print("The color is blue.") # Output: The color is blue.
elif color == "green":
    print("The color is green.") # Output: The color is green.
else:
    print("The color is not red, blue, or green.") # Output: The color is not red, blue, or green.