# If-Else Statement in Python
# An if-else statement is a conditional statement that allows you to execute one block of code if a certain condition is true, and another block of code if the condition is false. The syntax for an if-else statement in Python is as follows:
# if condition:
#     # block of code to be executed if the condition is true
# else:
#     # block of code to be executed if the condition is false
# For example:
age = 25
if age >= 18:
    print("You are voted to vote.") # Output: You are voted to vote.
    print("You can drive a car.") # Output: You can drive a car.
else:
    print("You are not voted to vote.") # Output: You are not voted to vote.
    print("You cannot drive a car.") # Output: You cannot drive a car.