# Relational Operators in Python
# It is also called as comparison operators. They are used to compare two values and return a boolean result (True or False) based on the comparison. Relational operators are essential for making decisions in programming, as they allow you to evaluate conditions and control the flow of your program based on those conditions.
# Relational operators are used to compare two values and return a boolean result (True or False). In Python, the relational operators are:
# 1. Equal to (==): This operator checks if the values of two operands are equal. For example, 5 == 5 will return True, while 5 == 10 will return False.
# 2. Not equal to (!=): This operator checks if the values of two operands are not equal. For example, 5 != 10 will return True, while 5 != 5 will return False.
# 3. Greater than (>): This operator checks if the value of the left operand is greater than the value of the right operand. For example, 10 > 5 will return True, while 5 > 10 will return False.
# 4. Less than (<): This operator checks if the value of the left operand is less than the value of the right operand. For example, 5 < 10 will return True, while 10 < 5 will return False.
# 5. Greater than or equal to (>=): This operator checks if the value of the left operand is greater than or equal to the value of the right operand. For example, 10 >= 5 will return True, while 5 >= 10 will return False.
# 6. Less than or equal to (<=): This operator checks if the value of the left operand is less than or equal to the value of the right operand. For example, 5 <= 10 will return True, while 10 <= 5 will return False.
# You can use these relational operators to compare numbers, strings, and other data types in Python. For example:
a = 10
b = 5
c = 10
print(a == b)  # False
print(a == c) # True
print(a != c) # False
print(a != b)  # True
print(a > b)   # True
print(a < b)   # False
print(a >= c)  # True
print(a <= c)  # True
# In this example, we compare the values of a, b, and c using the relational operators and print the results.