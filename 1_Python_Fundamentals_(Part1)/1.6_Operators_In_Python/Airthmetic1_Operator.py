# Arithmetic Operators in Python
# In Python, arithmetic operators are used to perform mathematical operations on numbers. The basic arithmetic operators include addition (+), subtraction (-), multiplication (*), division (/), modulus (%), exponentiation (**), and floor division (//). Here are some examples of how to use these operators in Python:
a = 10
b = 5
# Addition
c = a + b
print(c) # Output: 15 (addition operator returns the sum of the two numbers)
# Subtraction
d = a - b
print(d) # Output: 5 (subtraction operator returns the difference of the two numbers)
# Multiplication
e = a * b
print(e) # Output: 50 (multiplication operator returns the product of the two numbers)
# Division
f = a / b
print(f) # Output: 2.0 (division of two integers results in a float)
# Modulus
g = a % b
print(g) # Output: 0 (modulus operator returns the remainder of the division)
# Exponentiation
h = a ** b
print(h) # Output: 100000 (exponentiation operator raises the first number to the power of the second number)
# Floor Division
i = a // b
print(i) # Output: 2 (floor division operator returns the quotient of the division, rounded down to the nearest integer)
# in this example, we perform various arithmetic operations on the variables a and b using the different arithmetic operators and print the results. You can also use these operators with other data types, such as floats and complex numbers, to perform similar operations.