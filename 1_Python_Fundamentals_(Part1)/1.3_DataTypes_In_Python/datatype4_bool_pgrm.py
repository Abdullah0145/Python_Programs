# Boolean Data Type in Python
# In Python, a boolean is a data type that can have one of two values: True or False.
# To create a boolean variable, simply assign True or False to a variable name.
# For example:

num1 = 2
isPrime = True # boolean data type

print(isPrime) # Output: True
print(type(isPrime)) # Output: <class 'bool'>

num2 = 6
isPrime = False # boolean data type

print(isPrime) # Output: False
print(type(isPrime)) # Output: <class 'bool'>