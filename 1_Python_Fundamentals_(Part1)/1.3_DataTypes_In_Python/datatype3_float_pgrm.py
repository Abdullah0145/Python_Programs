# Float Data Type in Python
# In Python, a float is a number that has a decimal point. It can be positive (1.5, 2.7, 3.14), negative (-1.5, -2.7, -3.14), or zero (0.0).
# To create a float variable, simply assign a number with a decimal point to a variable name. 
# For example:

name = "shradha" # string data type
age = 35        # integer data type
PI = 3.14       # float data type

print(PI) # Output: 3.14
print(type(PI)) # Output: <class 'float'>