# Integer Data Type in Python
# In Python, an integer is a whole number that can be positive (1, 2, 3), negative (-1, -2, -3), or zero (0). It does not have a decimal point.
# To create an integer variable, simply assign a whole number to a variable name. 
# For example:

name= "shradha" # string data type
age = 35        # integer data type
PI = 3.14       # float data type

print(age) # Output: 35
