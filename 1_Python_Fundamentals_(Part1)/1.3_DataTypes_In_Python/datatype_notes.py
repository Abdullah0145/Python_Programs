# Data Types in Python
# In Python, there are several built-in data types that are used to store different kinds of data. The most common data types include:
# 1. Integer (int): Represents whole numbers, both positive(1, 2, 3) and negative(-1, -2, -3), without a decimal point. For example: 1, -5, 0.
# 2. String (str): Represents a sequence of characters enclosed in single quotes (''), double quotes (""), or triple quotes ("""). For example: "Hello", 'Python', """This is a string.""".
# 3. Float (float): Represents numbers that have a decimal point. For example: 3.14, -0.001, 0.0.
# 4. Boolean (bool): Represents a value that can be either True or False. For example: True, False.
# 5.NoneType (None): Represents the absence of a value or a null value. For example: None.
# To create a variable of a specific data type, simply assign a value of that type to a variable name. For example:
name = "shradha"  # str
age = 35          # int
height = 5.8      # float
isStudent = True   # bool
isPrime = None     # NoneType

print(name) # Output: shradha
print(type(name)) # Output: <class 'str'>
print(age) # Output: 35
print(type(age)) # Output: <class 'int'>
print(height) # Output: 5.8
print(type(height)) # Output: <class 'float'>
print(isStudent) # Output: True
print(type(isStudent)) # Output: <class 'bool'>
print(isPrime) # Output: None
print(type(isPrime)) # Output: <class 'NoneType'>