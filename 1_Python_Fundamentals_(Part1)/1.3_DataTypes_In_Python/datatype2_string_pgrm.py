# String Data Type in Python
# In Python, a string is a sequence of characters enclosed in single quotes (''), or double quotes (""), or triple quotes ("""). Strings are immutable, meaning their values cannot be changed after creation.
# To create a string variable, simply assign a sequence of characters to a variable name.
# For example:

name = "shradha" # string data type
age = 35        # integer data type
PI = 3.14       # float data type

print(name) # Output: shradha
print(type(name)) # Output: <class 'str'>