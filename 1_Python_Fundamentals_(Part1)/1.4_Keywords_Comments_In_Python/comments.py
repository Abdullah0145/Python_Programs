# This is a comment in Python
# Comments are used to explain the code and make it more readable. They are ignored by the Python interpreter and do not affect the execution of the program.
# In Python, you can create a comment by using the hash symbol (#) followed by the comment text. For example:

# This is a single-line comment
print("Hello, World!") # This is an inline comment (the comment is on the same line as the code)

# You can also create multi-line comments by using triple quotes (''' or """). For example:
'''This is another multi-line comment.
It can also span multiple lines.'''

"""
This is a multi-line comment.
It can span multiple lines.
"""